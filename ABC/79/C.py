n=int(input())
a=[0]*4
for i in range(3,-1,-1):
    a[i]=n%10
    n=n//10
if a[0]+a[1]+a[2]+a[3]==7:
    print("{0}+{1}+{2}+{3}=7".format(a[0],a[1],a[2],a[3]))
elif a[0]+a[1]+a[2]-a[3]==7:
    print("{0}+{1}+{2}-{3}=7".format(a[0],a[1],a[2],a[3]))
elif a[0]+a[1]-a[2]+a[3]==7:
    print("{0}+{1}-{2}+{3}=7".format(a[0],a[1],a[2],a[3]))
elif a[0]-a[1]+a[2]+a[3]==7:
    print("{0}-{1}+{2}+{3}=7".format(a[0],a[1],a[2],a[3]))
elif a[0]+a[1]-a[2]-a[3]==7:
    print("{0}+{1}-{2}-{3}=7".format(a[0],a[1],a[2],a[3]))
elif a[0]-a[1]-a[2]+a[3]==7:
    print("{0}-{1}-{2}+{3}=7".format(a[0],a[1],a[2],a[3]))
elif a[0]-a[1]+a[2]-a[3]==7:
    print("{0}-{1}+{2}-{3}=7".format(a[0],a[1],a[2],a[3]))
else:
    print("{0}+{1}+{2}+{3}=7".format(a[0],a[1],a[2],a[3]))